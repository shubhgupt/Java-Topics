
package javaapplication1;

public class MathClass {
    public static void main(String[] args) {
        System.out.println(Math.abs(-7.89));
        
        System.out.println(Math.max(78, 67));
        
        System.out.println(Math.min(67.4,45));
        
        System.out.println(Math.toRadians(90));
        
        System.out.println(Math.toDegrees(5));
        
        System.out.println(Math.sin(0.67));//value should be in radians
        
        System.out.println(Math.cos(1.89));
        
        System.out.println(Math.tan(1.3));
        
        System.out.println(Math.asin(0.67));//sin inverse
        
        System.out.println(Math.acos(1.89));
        
        System.out.println(Math.atan(1.3));
        
        System.out.println(Math.exp(4));//exponantial
        
        System.out.println(Math.log(67));//natural log
        
        System.out.println(Math.pow(4,6));
        
        System.out.println(Math.random());
        
        System.out.println(Math.sqrt(35));
        
        System.out.println(Math.E);//value of e
        
        System.out.println(Math.PI);//value of PI
    }
}
